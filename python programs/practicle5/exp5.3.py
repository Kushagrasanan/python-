# Creating a list
my_list = [3, 1, 4, 1, 5, 9]

# Sorting the list
my_list.sort()
print("Sorted list:", my_list)

# Appending an element to the list
my_list.append(2)
print("After appending:", my_list)

# Removing an element from the list
my_list.remove(4)
print("After removing:", my_list)
